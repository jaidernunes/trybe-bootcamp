// Make a program that, given a set value on a constant, return "positive" if this value is positive, "negative" if negative and "zero" otherwise.
const number = 0
if (number > 0){
    console.log("positive")
} else if (number < 0){
    console.log("negative")
} else if (number == 0) {
    console.log("zero")
} else {
    console.log("error: not a valid number")
}